/**
 * Check if a date is a Macau Public Holiday in 2026
 */
const isPublicHoliday = (date) => {
  const year = date.getFullYear();
  const month = date.getMonth() + 1;
  const day = date.getDate();
  const dateStr = `${year}-${month.toString().padStart(2, '0')}-${day.toString().padStart(2, '0')}`;
  
  // 2026 Macau Public Holidays
  const holidays2026 = [
    '2026-01-01', // New Year
    '2026-02-17', '2026-02-18', '2026-02-19', // Lunar New Year
    '2026-04-03', '2026-04-04', '2026-04-05', '2026-04-06', '2026-04-07', // Easter & Cheng Ming
    '2026-05-01', '2026-05-24', '2026-05-25', // Labour & Buddha
    '2026-06-19', // Dragon Boat
    '2026-09-26', '2026-09-28', // Mid-Autumn
    '2026-10-01', '2026-10-02', '2026-10-18', '2026-10-19', // National Day & Chong Yeung
    '2026-11-02', // All Souls
    '2026-12-08', '2026-12-20', '2026-12-21', '2026-12-22', '2026-12-24', '2026-12-25' // Dec holidays
  ];
  
  return holidays2026.includes(dateStr);
};

/**
 * Shared helper to calculate next N arrivals
 */
const calculateNextArrivals = (timetable, count = 2) => {
  const now = new Date();
  const gmt8Offset = 8 * 60;
  const localOffset = now.getTimezoneOffset();
  const gmt8Time = new Date(now.getTime() + (gmt8Offset + localOffset) * 60000);
  
  const day = gmt8Time.getDay();
  const hour = gmt8Time.getHours();
  const minute = gmt8Time.getMinutes();
  
  let schedule;
  if (isPublicHoliday(gmt8Time) || day === 0 || day === 6) {
    schedule = timetable.weekend;
  } else if (day === 5) {
    schedule = timetable.fri;
  } else {
    schedule = timetable.mon_thu;
  }
  
  const results = [];
  let currentHour = hour;
  while (results.length < count && currentHour <= 23) {
    const minutes = schedule[currentHour];
    if (minutes) {
      for (let m of minutes) {
        if (currentHour > hour || m > minute) {
          const diff = (currentHour - hour) * 60 + (m - minute);
          results.push(diff);
          if (results.length === count) break;
        }
      }
    }
    currentHour++;
  }
  
  // If no future arrivals found today, check if we're completely past last train
  if (results.length === 0) {
    return null; // Special marker for "Out of Service"
  }
  
  return results;
};

/**
 * Get the first and last train times for a given station timetable
 */
const getFirstLastTimes = (timetable) => {
  const getTimes = (schedule) => {
    if (!schedule) return null;
    const hours = Object.keys(schedule).map(Number).sort((a, b) => a - b);
    if (hours.length === 0) return null;
    
    const firstHour = hours[0];
    const lastHour = hours[hours.length - 1];
    
    const firstMin = Math.min(...schedule[firstHour]);
    const lastMin = Math.max(...schedule[lastHour]);
    
    return {
      first: `${firstHour.toString().padStart(2, '0')}:${firstMin.toString().padStart(2, '0')}`,
      last: `${lastHour.toString().padStart(2, '0')}:${lastMin.toString().padStart(2, '0')}`
    };
  };

  return {
    mon_thu: getTimes(timetable.mon_thu),
    fri: getTimes(timetable.fri),
    weekend: getTimes(timetable.weekend)
  };
};

